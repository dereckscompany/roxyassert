test_that("@genassert/@exportassert drive the on-disk roclet_output pipeline idempotently", {
  dir <- withr::local_tempdir()
  dir.create(file.path(dir, "R"))
  writeLines(c(
    "Package: ondisk", "Title: t", "Version: 0.0.0", "Description: t.",
    "License: MIT + file LICENSE", "Encoding: UTF-8"
  ), file.path(dir, "DESCRIPTION"))
  writeLines(c(
    "#' Shapes.",
    "#' @type Ack (data.table) one row:",
    "#' - id (character) the id.",
    "#' @genassert",
    "#' @exportassert",
    "#' @name shapes",
    "NULL"
  ), file.path(dir, "R", "shapes.R"))
  base_ns <- c("# Generated by roxygen2: do not edit by hand", "")
  rd <- file.path(dir, "man", "roxyassert-generated-asserts.Rd")

  run <- function() {
    writeLines(base_ns, file.path(dir, "NAMESPACE")) # mimic the namespace roclet
    blocks <- suppressMessages(roxygen2::parse_package(dir))
    results <- roxygen2::roclet_process(contract_roclet(), blocks, env = NULL, base_path = dir)
    roxygen2::roclet_output(contract_roclet(), results, base_path = dir)
  }

  run()
  ns1 <- readLines(file.path(dir, "NAMESPACE"))
  expect_true(any(grepl("export(assert_type_Ack)", ns1, fixed = TRUE)))
  expect_true(file.exists(rd))
  expect_true(any(grepl("\\alias{assert_type_Ack}", readLines(rd), fixed = TRUE)))
  rd1 <- readLines(rd)

  run() # second pass: NAMESPACE + Rd must be byte-identical
  expect_identical(readLines(file.path(dir, "NAMESPACE")), ns1)
  expect_identical(readLines(rd), rd1)
})
