# @genassert (generate a standalone assert from a @type, even when unused) and
# @exportassert (export the generated helpers), plus the NAMESPACE writer.

test_that("@genassert emits a standalone assert_type_<Name>()", {
  text <- "
    #' Shapes.
    #' @type Ack (data.table) one row:
    #' - id (character) the id.
    #' - n (numeric) a number.
    #' @genassert
    #' @name shapes
    NULL
  "
  code <- proc_code(text)
  expect_true(any(grepl("^assert_type_Ack <- function\\(value\\) \\{", code)))
  expect_true(any(grepl("assert_data_table\\(value\\)", code)))
  expect_true(any(grepl('assert_has_columns\\(value, c\\("id", "n"\\)\\)', code)))
  expect_true(any(grepl("return\\(invisible\\(value\\)\\)", code)))
})

test_that("@genassert emits a helper for every @type in the block", {
  text <- "
    #' Shapes.
    #' @type A (data.table) one row:
    #' - x (character) x.
    #' @type B (data.table) one row:
    #' - y (numeric) y.
    #' @genassert
    #' @name shapes
    NULL
  "
  code <- proc_code(text)
  expect_true(any(grepl("^assert_type_A <- function", code)))
  expect_true(any(grepl("^assert_type_B <- function", code)))
})

