test_that(".ra_write_rd_exports writes a documenting Rd (not roxygen2-headed) and strips when empty", {
  dir <- file.path(tempdir(), "ra_rd_test")
  dir.create(file.path(dir, "man"), recursive = TRUE, showWarnings = FALSE)
  on.exit(unlink(dir, recursive = TRUE), add = TRUE)
  code <- c(
    "assert_type_OrderAck <- function(value) {",
    "  assert_data_table(value)",
    "}",
    "assert_args_submit <- function(symbol) {",
    "  assert_scalar_character(symbol)",
    "}"
  )
  rd <- file.path(dir, "man", "roxyassert-generated-asserts.Rd")

  .ra_write_rd_exports(dir, c("assert_type_OrderAck", "assert_args_submit"), code)
  expect_true(file.exists(rd))
  txt <- readLines(rd)
  expect_true(any(grepl("\\alias{assert_type_OrderAck}", txt, fixed = TRUE)))
  expect_true(any(grepl("\\alias{assert_args_submit}", txt, fixed = TRUE)))
  expect_true(any(grepl("assert_type_OrderAck(value)", txt, fixed = TRUE)))   # usage recovered
  expect_true(any(grepl("assert_args_submit(symbol)", txt, fixed = TRUE)))
  expect_true(any(grepl("\\keyword{internal}", txt, fixed = TRUE)))
  expect_false(any(grepl("Generated by roxygen2", txt)))                       # rd roclet won't purge it

  .ra_write_rd_exports(dir, character(), character())                          # empty removes it
  expect_false(file.exists(rd))
})

test_that(".ra_write_namespace_exports appends, is idempotent, and strips when empty", {
  dir <- file.path(tempdir(), "ra_ns_test")
  dir.create(dir, showWarnings = FALSE)
  on.exit(unlink(dir, recursive = TRUE), add = TRUE)
  ns <- file.path(dir, "NAMESPACE")
  writeLines(c("# Generated by roxygen2: do not edit by hand", "", "export(foo)"), ns)

  .ra_write_namespace_exports(dir, c("assert_type_Ack", "assert_args_foo"))
  out <- readLines(ns)
  expect_true(any(grepl("export(assert_type_Ack)", out, fixed = TRUE)))
  expect_true(any(grepl("export(assert_args_foo)", out, fixed = TRUE)))
  expect_true(any(grepl("export(foo)", out, fixed = TRUE))) # original untouched

  # Re-running replaces (not duplicates) the managed block.
  .ra_write_namespace_exports(dir, c("assert_type_Ack", "assert_args_foo"))
  expect_equal(sum(grepl("export(assert_type_Ack)", readLines(ns), fixed = TRUE)), 1L)

  # Empty removes the managed block, leaving the original directives.
  .ra_write_namespace_exports(dir, character())
  out2 <- readLines(ns)
  expect_false(any(grepl("assert_type_Ack", out2)))
  expect_false(any(grepl("managed", out2)))
  expect_true(any(grepl("export(foo)", out2, fixed = TRUE)))
})

